# Merging two lists index-wise

rollNumbers = [1, 2, 3, 4, 5]
student_names = ["Rohit D", "Parth S", "Ken T", "Sarang M", "Alim S"]

student_database = list(zip(rollNumbers, student_names))

print("Roll numbers:", rollNumbers)
print("Student names:", student_names)
print("Student database:", student_database)